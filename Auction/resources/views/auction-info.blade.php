            <h2 class="question" id="question-terms">Термины</h2>
            <p class="answer">
               <p class="answer-item"><b>Лот</b> — это предмет, который продавец выставил на аукцион</p>
               <p class="answer-item"><b>Ставка</b> — это предложение от участника продавцу большей цены за предмет</p>
               <p class="answer-item"><b>Победитель</b> — пользователь, сделавший максимальную среди всех участников ставку</p>
               <p class="answer-item"><b>Торговая фаза</b> — это период времени, когда участники могут делать ставки на предметы</p>
               <p class="answer-item"><b>Автовыкуп</b> — это функция, позволяющая победителю сразу же купить предмет по указанной цене, если продавец включил эту функцию</p>
               <p class="answer-item"><b>Начальная цена</b> — на каждый лот устанавливается начальная цена, с нее начинается аукцион. Нельзя поставить ставку меньше этой суммы</p>
            </p>
