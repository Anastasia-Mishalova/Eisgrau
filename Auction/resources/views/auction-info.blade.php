            <h2 class="question" id="question-whats-auction">Что такое аукцион?</h2>
            <p class="answer"><b>Аукцион</b> — это способ продажи товаров или услуг, при котором покупатели делают ставки, чтобы купить предмет. Это может быть что угодно: от антиквариата до автомобилей или недвижимости. Аукционы могут проходить как вживую, так и онлайн.
